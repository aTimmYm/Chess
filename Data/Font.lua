function Font.new()
    return setmetatable({}, Font)
end

function Font:draw(text, x, y, fg, bg)
    if not term.getGraphicsMode() then
        error("Сначала включи graphics mode: term.setGraphicsMode(1) или (2)", 2)
    end

    -- term.setFrozen(true)

    local px = x
    for i = 1, #text do
        local ch = text:sub(i, i)
        local glyph = self.glyphs[ch] or self.glyphs["?"]

        for row = 1, self.charHeight do
            local line = glyph[row]
            for col = 1, self.charWidth do
                local pixel = line:sub(col, col)
                local sx, sy = px + col - 1, y + row - 1

                if pixel == "#" then
                    term.setPixel(sx, sy, fg)
                elseif bg and pixel == " " then
                    term.setPixel(sx, sy, bg)
                end
            end
        end
        px = px + self.charWidth + self.interval
    end

    -- term.setFrozen(false)
end

return Font